    public class incoming_reader implements Runnable
    {
        @Override
        public void run()
        {
            String[] i;
            String stream;
            
            try{
           
                while((stream=r.readLine())!=null)
                {
                    i=stream.split(":");
                    if(!stream.equals("CU"))
                    {
                    switch (i[2]) {
                        case "connect":
                            status.setText("CONNECTED");
                            area.append(i[0]+": "+i[1]+"\n");
                            break;
                        case "disconnect":
                            area.append(i[0]+": "+i[1]+"\n");
                            area.append(i[0]+" IS NOW OFFLINE.\n");
                            break;
                        case "chat":
                            area.append(i[0]+":"+i[1]+"\n");
                            break;
                        default:
                            break;
                    }
                    }
                    else
                        status.setText("CHANGE USERNAME(CU)");
                }
            }
            catch(Exception e)
            {
                
            }
        }
    }
    
